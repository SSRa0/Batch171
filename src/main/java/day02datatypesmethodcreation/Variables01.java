package day02datatypesmethodcreation;

public class Variables01 {
    public static void main(String[] args) {

        //Ornek 1 : Sehir ismi icin bir variable olusturun ve one "Ankara" sonrada "izmir" degeri atayip ekrana yazdiriniz.
        String cityName = "Ankara";
        System.out.println(cityName);
        cityName = "izmir";
        System.out.println(cityName);

        //Ornek 2 : Sehir plaka kodu bir variable olusturun ve once "06" sonra da "35" degeri atayip ekrana yazdirin
        byte city1 = 06;
        System.out.println(city1);
        city1 = 35;
        System.out.println(city1);

        //Ornek 3 : Basarli mi sorusu icin bir veriable olusturun ve once false sonra da true degeri atayip ekrana yazdirin.
        boolean isSuccesfull = false;
        System.out.println(isSuccesfull);
        isSuccesfull= true;
        System.out.println(isSuccesfull);






    }
}
